package com.koalog.jcs.constraint;

/**
 * This exception is thrown when a constraint is malformed.
 * @author Yan Georget
 */
public class InvalidConstraintException extends RuntimeException {
    //------------------------------------------------------------------------
    // CONSTRUCTORS
    //------------------------------------------------------------------------
    /**
     * Main constructor.
     * @param message a message
     */
    public InvalidConstraintException(String message) {
        super(message);
    }
    
    /**
     * Auxilliary constructor.
     */
    public InvalidConstraintException() {
        super();
    }
}
