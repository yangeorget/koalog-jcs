package com.koalog.jcs.constraint;

import com.koalog.jcs.variable.Variable;

/**
 * This class represents a ternary constraint
 * (constraint over 3 variables).
 * @author Yan Georget
 */
public abstract class TernaryConstraint extends BaseConstraint {
  //------------------------------------------------------------------------
  // CONSTRUCTORS
  //------------------------------------------------------------------------
  /** 
   * Constructs a ternary constraint.
   * @param x a Variable
   * @param y a Variable
   * @param z a Variable
   */
  public TernaryConstraint(Variable x, Variable y, Variable z) {
      super(new Variable[] {x,y,z});
  }
}
