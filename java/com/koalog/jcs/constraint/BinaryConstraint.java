package com.koalog.jcs.constraint;

import com.koalog.jcs.variable.Variable;

/**
 * This class represents a binary constraint 
 * (a constraint over 2 variables).
 * @author Yan Georget
 */
public abstract class BinaryConstraint extends BaseConstraint {
  //------------------------------------------------------------------------
  // CONSTRUCTORS
  //------------------------------------------------------------------------
  /** 
   * Constructs a binary constraint.
   * @param var1 a Variable
   * @param var2 a Variable
   */
  public BinaryConstraint(Variable var1, Variable var2) {
      super(new Variable[] {var1, var2});
  }
}
