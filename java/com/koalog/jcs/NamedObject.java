package com.koalog.jcs;

/**
 * An interface for named objects.
 *
 * @since 2.3
 * @author Yan Georget
 */
public interface NamedObject {
    //------------------------------------------------------------------------
    // CONSTANTS
    //------------------------------------------------------------------------
    /** A separator to separate the unid. */
    String UNID_SEPARATOR = "#";

    //------------------------------------------------------------------------
    // ABSTRACT METHODS
    //------------------------------------------------------------------------
    /**
     * Gets the name.
     * @return a string
     */
    String getName();

    /**
     * Gets the unid.
     * @return an integer
     */
    int getUnid();

    /**
     * Gets the full name.
     * @return a string
     */
    String getFullName();
}
